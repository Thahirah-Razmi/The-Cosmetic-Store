    private void performSearch() {
        String category = comboBoxCategory.getSelectedItem().toString().split("-")[0]; // Get the category_pk
        String name = txtName.getText();
        String priceStr = txtPrice.getText();

        String query = "SELECT * FROM product INNER JOIN category ON product.category_fk = category.category_pk WHERE category.category_pk = '" + category + "'"
                + " AND product.name LIKE '%" + name + "%'"
                + " AND product.price LIKE '%" + priceStr + "%'";

        DefaultTableModel model = (DefaultTableModel) productDetailsTable.getModel();
        model.setRowCount(0); // Clear existing rows

        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("product_pk"), rs.getString("name"), rs.getString("quantity"), rs.getString("price"), rs.getString("description"), rs.getString("category_fk"), rs.getString(8)});
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

