    private void populateProductDetailsTable() {
        DefaultTableModel model = (DefaultTableModel) productDetailsTable.getModel();
        model.setRowCount(0); // Clear existing rows

        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from product inner join category on product.category_fk = category.category_pk");

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("product_pk"), rs.getString("name"), rs.getString("quantity"),
                    rs.getString("price"), rs.getString("description"), rs.getString("category_fk"), rs.getString(8)});
            }

            con.close(); // Close the connection
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void getAllCategory() {
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from category");
            comboBoxCategory.removeAllItems();
            comboBoxCategory.addItem("No Category");
            while (rs.next()) {
                comboBoxCategory.addItem(rs.getString("category_pk") + "-" + rs.getString("name"));
                //1-Hair Care
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

